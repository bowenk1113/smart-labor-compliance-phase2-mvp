export const t = (key) => messages[locale.value]?.[key] || messages['zh-CN'][key] || key

const statusKeys = {
  success: 'statusSuccess',
  failed: 'statusFailed',
  pending: 'statusPending',
  processing: 'statusProcessing',
  resolved: 'statusResolved',
  ignored: 'statusIgnored',
  active: 'statusActive',
  disabled: 'statusDisabled'
}

const riskKeys = {
  high: 'riskHigh',
  medium: 'riskMedium',
  low: 'riskLow'
}

const difficultyKeys = {
  normal: 'difficultyNormal',
  edge: 'difficultyEdge'
}

const roleKeys = {
  super_admin: 'roleSuperAdmin',
  tenant_admin: 'roleTenantAdmin',
  operator: 'roleOperator'
}

export const useI18n = () => {
  const currentLocale = computed(() => locale.value)
  const setLocale = (value) => {
    locale.value = value
    localStorage.setItem('locale', value)
  }
  const formatDateTime = (time) => {
    if (!time) return ''
    return new Date(time).toLocaleString(locale.value === 'en' ? 'en-US' : 'zh-CN')
  }
  const statusLabel = (status) => status ? t(statusKeys[status] || status) : ''
  const riskLabel = (risk) => risk ? t(riskKeys[risk] || risk) : ''
  const difficultyLabel = (difficulty) => difficulty ? t(difficultyKeys[difficulty] || difficulty) : ''
  const roleLabel = (role, fallback = '') => role ? t(roleKeys[role] || role) : fallback
  return { locale: currentLocale, setLocale, t, formatDateTime, statusLabel, riskLabel, difficultyLabel, roleLabel }
}
